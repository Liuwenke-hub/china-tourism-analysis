"""生成《中国旅游与经济发展可视化分析报告》docx。

运行：python src/generate_report.py  ->  output/中国旅游与经济发展可视化分析报告.docx
"""
from pathlib import Path
from docx import Document
from docx.shared import Pt, Cm, RGBColor
from docx.enum.text import WD_ALIGN_PARAGRAPH

from config import OUTPUT_DIR, CHARTS_DIR

AUTHOR = "数据分析项目组"                     # 报告署名
YEAR_RANGE = "2005–2022"

doc = Document()

# 基础样式
style = doc.styles["Normal"]
style.font.name = "Microsoft YaHei"
style.font.size = Pt(11)


def _h(text, level=1):
    doc.add_heading(text, level=level)


def _p(text, bold=False, size=11, align=None, color=None):
    para = doc.add_paragraph()
    run = para.add_run(text)
    run.bold = bold
    run.font.size = Pt(size)
    if color:
        run.font.color.rgb = RGBColor(*color)
    if align:
        para.alignment = align
    return para


def _fig(name, caption):
    path = CHARTS_DIR / name
    if path.exists():
        doc.add_picture(str(path), width=Cm(15.5))
        doc.paragraphs[-1].alignment = WD_ALIGN_PARAGRAPH.CENTER
        cap = doc.add_paragraph()
        r = cap.add_run(f"图：{caption}")
        r.italic = True
        r.font.size = Pt(9)
        cap.alignment = WD_ALIGN_PARAGRAPH.CENTER


# ---------------- 封面 ----------------
title = doc.add_paragraph()
title.alignment = WD_ALIGN_PARAGRAPH.CENTER
tr = title.add_run("中国旅游与经济发展\n可视化分析报告")
tr.bold = True
tr.font.size = Pt(26)
tr.font.color.rgb = RGBColor(0x1f, 0x6f, 0x8b)

sub = doc.add_paragraph()
sub.alignment = WD_ALIGN_PARAGRAPH.CENTER
sr = sub.add_run(f"基于 Python 的多维统计数据分析（{YEAR_RANGE}）")
sr.font.size = Pt(13)
sr.font.color.rgb = RGBColor(0x55, 0x55, 0x55)

for _ in range(4):
    doc.add_paragraph()
info = doc.add_paragraph()
info.alignment = WD_ALIGN_PARAGRAPH.CENTER
info.add_run(f"作者：{AUTHOR}\n数据范围：{YEAR_RANGE}\n").font.size = Pt(12)

doc.add_page_break()

# ---------------- 摘要 ----------------
_h("摘要", 1)
_p("本报告以国家统计局、文化和旅游部等公开统计年鉴，以及海南省旅游和文化广电体育厅"
  "月度统计为基础，整合 10 类数据集，运用 Python（pandas + matplotlib/seaborn + pyecharts）"
  "构建了覆盖宏观经济、国内/国际旅游、区域差异与海南专题的多维可视化分析体系。"
  "研究共生成 30 张静态分析图表与 13 张交互式图表，系统揭示了旅游经济与宏观发展的耦合关系，"
  "并提出了面向目的地运营与客源结构优化的可操作建议。")

# ---------------- 第一章 ----------------
_h("第一章 宏观经济发展趋势分析", 1)
_p("宏观经济的持续增长是旅游产业发展的根基。本章从 GDP 总量与增长率、居民收入与消费水平"
  "三个维度刻画中国经济的基本盘及其演变节奏。")
_fig("01_中国GDP总量年度趋势.png", "中国GDP总量年度趋势（万亿元）")
_fig("02_中国GDP年度增长率.png", "中国GDP年度增长率（%）")
_p("2005—2022 年间，中国 GDP 由约 18.4 万亿元增长至 123.4 万亿元，年均保持中高速增长；"
  "2020 年受疫情冲击增速回落至约 2.3%，随后在 2021 年修复、2022 年再度承压。"
  "经济增长为居民旅游消费能力的提升提供了根本支撑。")
_fig("03_中国居民收入与消费趋势.png", "全国居民人均可支配收入与消费水平（元）")
_p("居民人均可支配收入与消费水平同步抬升，表明居民在基本生存型消费之外，具备了更强的"
  "发展型、享受型消费（含旅游）支付能力，构成旅游业长期向好的需求侧基础。")

# ---------------- 第二章 ----------------
_h("第二章 旅游业发展趋势分析", 1)
_p("本章聚焦旅游产业自身的发展曲线，分别刻画国内旅游与国际旅游的规模演变与增长波动。")
_fig("04_国内旅游人次与收入趋势.png", "国内旅游人次与收入趋势（双轴）")
_p("国内旅游在 2020 年遭遇断崖式下滑（游客量同比下降约 52%），2021 年短暂反弹（同比 +12.7%），"
  "2022 年再度承压（同比约 -22%）。2020—2022 年持续受疫情扰动，尚未恢复至疫情前水平，"
  "反映出外部冲击下旅游需求的脆弱性。")
_fig("05_国际旅游收入与游客趋势.png", "国际旅游收入与游客趋势（双轴）")
_fig("06_国际旅游外汇收入增长率.png", "国际旅游外汇收入增长率（%）")
_fig("07_接待国外游客增长率.png", "接待国外游客增长率（%）")
_p("国际旅游（外汇收入与接待国外游客）在 2005—2019 年稳步上行，但因 2020 年后全球疫情"
  "及跨境管制，至本报告数据窗口（截至 2019 年）仍呈现健康增长态势。")

# ---------------- 第三章 ----------------
_h("第三章 旅游与国民经济指标相关性分析", 1)
_p("为量化旅游经济与宏观指标间的耦合强度，本章采用 Pearson 相关分析与线性回归建模。")
_fig("08_国家宏观经济与旅游指标相关性热力图.png", "宏观经济与旅游指标相关性热力图")
_p("相关性热力图显示，GDP、居民收入、消费水平与国内旅游花费之间均存在极强正相关"
  "（r 普遍超过 0.95），说明旅游消费高度依赖宏观经济景气与居民购买力。")
_fig("09_GDP与国际旅游外汇收入散点图.png", "GDP 与国际旅游外汇收入散点图（含拟合线）")
_fig("10_人均收入与国内旅游收入散点图.png", "人均收入与国内旅游收入散点图（含拟合线）")
_fig("11_GDP与国内旅游人次散点图.png", "GDP 与国内旅游人次散点图（含拟合线）")
_fig("12_国际旅游收入与游客散点图.png", "国际旅游收入与游客散点图（含拟合线）")
_p("四组散点+线性回归进一步印证了上述关系：随着经济总量与居民收入扩张，旅游人次与花费"
  "近似线性抬升，回归模型可为旅游市场规模的宏观预测提供参考。")

# ---------------- 第四章 ----------------
_h("第四章 区域差异分析", 1)
_p("旅游发展的区域不平衡是产业政策关注的重点。本章从省份排名、重点省份趋势与分布差异"
  "三个层面展开。")
_fig("13_2022年各省GDP排名.png", "2022年各省GDP排名 Top15")
_fig("14_重点省份GDP年度趋势.png", "重点省份GDP年度趋势")
_fig("23_重点省份GDP增长率趋势.png", "重点省份GDP增长率趋势")
_fig("15_2019年各省国际旅游外汇收入排名.png", "2019年各省国际旅游外汇收入排名 Top15")
_fig("17_重点省份国际旅游外汇收入年度趋势.png", "重点省份国际旅游外汇收入年度趋势")
_p("东部沿海省份（广东、江苏、上海、北京等）在 GDP 与国际旅游外汇收入上均显著领先，"
  "区域间呈现明显的马太效应。")
_fig("16_各省国际旅游外汇收入分布箱线图.png", "各省国际旅游外汇收入分布箱线图")
_fig("19_各省接待国外游客分布箱线图.png", "各省接待国外游客分布箱线图")
_p("箱线图直观展示了省际国际旅游指标的量级差异（头部省份中位数可达尾部省份的数十倍），"
  "反映出目的地资源禀赋与基础设施的门槛效应。")
_fig("18_2019年各省接待国外游客排名.png", "2019年各省接待国外游客排名 Top15")
_fig("24_各省平均国际旅游外汇收入.png", "各省平均国际旅游外汇收入 Top15")
_fig("26_各省接待国外游客平均增长率.png", "各省接待国外游客平均增长率 Top15")
_fig("25_2019年各省GDP与接待国外游客散点图.png", "2019年各省GDP与接待国外游客散点图")
_p("GDP 规模与接待国外游客量总体正相关，但部分旅游资源型省份（如西部热点目的地）"
  "表现出高于其经济总量的国际客流吸纳能力，提示客源结构优化的差异化路径。")

# ---------------- 第五章 ----------------
_h("第五章 旅游业发展情况综合分析", 1)
_p("本章从产业供给与内部结构角度，综合考察旅游业发展指标间的协同关系。")
_fig("20_旅游业发展主要指标趋势.png", "旅游业发展主要指标趋势（指数化）")
