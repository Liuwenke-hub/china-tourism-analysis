_p("海南旅游呈现鲜明的季节性：每年 11 月至次年 3 月为接待高峰（避寒游需求主导），"
  "夏季相对平缓。这一规律提示目的地应在旺季前置调配住宿、交通与人力，淡季则侧重"
  "产品创新与客源培育，以实现全年均衡运营。")

# ---------------- 结论 ----------------
_h("结论", 1)
_p("1. 经济发展是旅游繁荣的根本前提：GDP、居民收入与旅游消费高度正相关（r>0.95）。")
_p("2. 国内旅游在 2020—2022 年持续受疫情冲击（2020 同比 -52%，2022 再度 -22%），"
  "尚未恢复至疫情前水平；国际旅游长期增长潜力仍在。")
_p("3. 区域间旅游发展不平衡突出，东部沿海领先，目的地资源禀赋决定客源吸纳上限。")
_p("4. 海南季节性规律明确，目的地运营应据此优化淡旺季资源调度。")
_p("5. 上述结论可为文旅企业客源结构优化、营销预算投放与产业规划提供量化依据。")

