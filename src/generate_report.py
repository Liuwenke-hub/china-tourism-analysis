_p("国内游客、国内旅游总花费、国际旅游外汇收入、旅行社与星级饭店数量等指标长期协同增长，"
  "产业供给端（旅行社、饭店）的扩张与需求端（游客、花费）形成正向循环；其中国内旅游"
  "贡献了旅游总收入的绝对主体，国际旅游为重要补充。")

# ---------------- 第六章 ----------------
_h("第六章 海南旅游专题分析", 1)
_p("海南作为国际旅游岛，其季节性规律对目的地运营调度具有典型参考意义。")
_fig("27_海南月度游客与收入趋势.png", "海南月度游客与收入趋势")
